public class Empresa {
    private Chefe[] chefes;
    private Vendedores[] vendedores;
    private Operario[] operarios;
    private Horista[] horistas;

    public Chefe[] getChefes() {
        return chefes;
    }

    public void setChefes(Chefe[] chefes) {
        this.chefes = chefes;
    }

    public Vendedores[] getVendedores() {
        return vendedores;
    }

    public void setVendedores(Vendedores[] vendedores) {
        this.vendedores = vendedores;
    }

    public Operario[] getOperarios() {
        return operarios;
    }

    public void setOperarios(Operario[] operarios) {
        this.operarios = operarios;
    }

    public Horista[] getHoristas() {
        return horistas;
    }

    public void setHoristas(Horista[] horistas) {
        this.horistas = horistas;
    }
}
